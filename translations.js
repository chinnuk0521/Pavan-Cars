let currentLanguage = localStorage.getItem('language') || 'en';

function setLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    applyTranslations();
    updateLanguageButton();
}

function applyTranslations() {
    // Translate all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            if (element.tagName === 'OPTION') {
                element.textContent = translations[currentLanguage][key];
            } else {
                element.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // Translate all placeholder attributes
    document.querySelectorAll('[data-placeholder]').forEach(element => {
        const key = element.getAttribute('data-placeholder');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            element.placeholder = translations[currentLanguage][key];
        }
    });
    
    // Translate all aria-label attributes
    document.querySelectorAll('[data-translate-aria]').forEach(element => {
        const key = element.getAttribute('data-translate-aria');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            element.setAttribute('aria-label', translations[currentLanguage][key]);
        }
    });
    
    // Update document language attribute
    document.documentElement.lang = currentLanguage;
    
    // Update body direction if needed (Telugu is LTR, so no change needed)
}

function updateLanguageButton() {
    const langBtn = document.getElementById('language-toggle');
    if (langBtn) {
        langBtn.innerHTML = currentLanguage === 'en' ? 'తెలుగు' : 'English';
    }
}

// Initialize translation system
document.addEventListener('DOMContentLoaded', () => {
    // Apply initial translations
    applyTranslations();
    updateLanguageButton();
    
    // Setup language toggle button
    const langToggle = document.getElementById('language-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', (e) => {
            e.preventDefault();
            setLanguage(currentLanguage === 'en' ? 'te' : 'en');
        });
    }
});
